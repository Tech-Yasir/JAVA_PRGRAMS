package com.control_statement;

public class SwicthCase {

	public static void main(String[] args) {
		int student = 0;  
		switch (student){  
		case 0:  
		System.out.println("number is 0");  
		break;  
		case 1:  
		System.out.println("number is 1");  
		break;  
		default:  
		System.out.println(student);  
		}  
		}  

	}


